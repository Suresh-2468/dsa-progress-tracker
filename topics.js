const topicTitle =
document.getElementById("topicTitle");

const topicDescription =
document.getElementById("topicDescription");

const conceptsContainer =
document.getElementById("conceptsContainer");

const problemsContainer =
document.getElementById("problemsContainer");

const topicProgress =
document.getElementById("topicProgress");


//  Progress Function
function updateProgress(){

    const solvedProblems =
    document.querySelectorAll(
        ".roadmap-problem.solved"
    ).length;

    const totalProblems =
    document.querySelectorAll(
        ".roadmap-problem"
    ).length;

    topicProgress.textContent =
    `${solvedProblems} / ${totalProblems}`;

}

// ====================================
// Render Topic
// ====================================

function renderTopic(topicKey){

    console.log(topicKey);
    console.log(topicsData[topicKey]);

    const topic =
    topicsData[topicKey];

    if(!topic) return;

    // Title

    topicTitle.textContent =
    topic.title;

    // Description

    topicDescription.textContent =
    topic.description;

    // Concepts

    conceptsContainer.innerHTML = "";

    let conceptsHTML = `

        <h2 class="section-title">

            Concepts

        </h2>

        <div class="concepts-list">

    `;

    topic.concepts.forEach(concept => {

        conceptsHTML += `

            <div class="concept-card">

                ✅ ${concept}

            </div>

        `;

    });

    conceptsHTML += `

        </div>

    `;

    conceptsContainer.innerHTML =
    conceptsHTML;

    // Problems

        problemsContainer.innerHTML = "";

            let problemsHTML = `

                <h2 class="section-title">

                    Problems

                </h2>

            `;

        topic.problems.forEach(problem => {

            let platformButtons = "";

            problem.platforms.forEach(platform => {

                platformButtons += `

                    <a href="${platform.link}"
                    target="_blank"
                    class="platform-btn">

                        ${platform.name}

                    </a>

                `;

            });

            problemsHTML += `

                <li class="roadmap-problem">

                    <div class="problem-left">

                        <div
                            class="solve-circle"
                            data-problem="${problem.name}">

                            <i class="ri-check-line"></i>

                        </div>

                        <div class="problem-content">

                            <span class="problem-link">

                                ${problem.name}

                            </span>

                            <div class="problem-platforms">

                                ${platformButtons}

                            </div>

                        </div>

                    </div>

                    <span
                    class="difficulty-badge ${problem.difficulty.toLowerCase()}">

                        ${problem.difficulty}

                    </span>

                </li>

            `;

        });

    problemsContainer.innerHTML =
    problemsHTML;

    updateProgress();

    console.log(
       document.querySelectorAll(
          ".roadmap-problem"
        ).length
    );

    const circles =
    document.querySelectorAll(
        ".solve-circle"
    );

    circles.forEach(circle => {

        const problemName =
        circle.dataset.problem;

        const parent =
        circle.closest(
            ".roadmap-problem"
        );

        // Load Saved State

        if(
            localStorage.getItem(
                problemName
            ) === "true"
        ){

            parent.classList.add(
                "solved"
            );

        }

        // Toggle State

        circle.addEventListener(
            "click",
            () => {

                parent.classList.toggle(
                    "solved"
                );

                localStorage.setItem(

                    problemName,

                    parent.classList.contains(
                        "solved"
                    )

                );

                updateProgress();

            }
        );

    });

}

// ====================================
// Topic Switching
// ====================================

const topicItems =
document.querySelectorAll(
    ".platform-item"
);

topicItems.forEach(item => {

    item.addEventListener(
        "click",
        () => {

            topicItems.forEach(el => {

                el.classList.remove(
                    "active"
                );

            });

            item.classList.add(
                "active"
            );

            renderTopic(
                item.dataset.topic
            );            

        }
    );

});
